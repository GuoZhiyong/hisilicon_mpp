/* the function of sensor set fps */
static HI_VOID cmos_fps_set(HI_FLOAT f32Fps, AE_SENSOR_DEFAULT_S *pstAeSnsDft)
{
    HI_U32 tp = 1692;
    HI_U32 u32VblankingLines = 0xFFFF;
    if ((f32Fps <= 30) && (f32Fps >= 0.5))
    {
        if(SENSOR_720P_30FPS_MODE == gu8SensorImageMode)
        {
            u32VblankingLines = tp * 30 / f32Fps + 3;
        }
    }
    else
    {
        printf("Not support Fps: %f\n", f32Fps);
        return;
    }

    sensor_write_register(0x2a, u32VblankingLines & 0xfc);
    sensor_write_register(0x2b, (u32VblankingLines & 0xff00) >> 8);
    
    pstAeSnsDft->f32Fps = f32Fps;
    pstAeSnsDft->u32MaxIntTime = 806;
    gu32FullLinesStd = 810;
    gu8Fps = f32Fps;
    pstAeSnsDft->u32LinesPer500ms = gu32FullLinesStd * f32Fps / 2;
    pstAeSnsDft->u32FullLinesStd = gu32FullLinesStd;

    return;
}

